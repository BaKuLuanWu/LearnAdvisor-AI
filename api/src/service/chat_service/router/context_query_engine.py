import re

class ContextQueryEngine:
    """
    规则引擎：
    1. 先检查负面规则（匹配则直接返回 False，用于拦截误判）。
    2. 再对正面规则计分（累加分数，达到阈值返回 True）。
    """

    def __init__(self, threshold=0.8):
        self.threshold = threshold

        # ---------- 负面规则：匹配即否定（用于拦截常见误判） ----------
        self.negative_rules = [
            # 包含否定词或后续非疑问语气的常见误判模式
            r"我刚才问了什么不重要",
            r"我刚刚问的问题不重要",
            r"我之前的问题不重要",
            r"上一句说了什么不重要",
            r"上一句问了什么不重要",
            r"你记得我刚刚说的话吗[，,]?哦",
            r"我刚才说的那个事情你明白了吗",
            r"我刚刚说的话你别在意",
            r"我之前的问题已经解决了",
            r"上一句说了什么并不重要",
            r"你记得我刚刚说的话吗\?* 哦我忘了",
            r"我刚才是不是说了什么奇怪的话",

            # 明显无关的查询（避免误触发正面弱规则）
            r"什么是机器学习",
            r"帮我查一下天气",
            r"今天天气怎么样",
            r"你吃饭了吗",
            r"这句话什么意思",
            # 可继续添加...
        ]

        # ---------- 正面规则：计分累加（仅针对用户询问自己刚才所说/所问） ----------
        self.positive_rules = [
            # ===== 强模式（1.0分）：明确询问自己之前的内容 =====
            # 带“什么”的变体
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)问了什么", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)说了什么", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)的问题是什么", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)说的是什么", 1.0),
            # 带“啥”的口语变体
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)问了啥", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)说了啥", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)的问题(是啥|是啥啊)", 1.0),
            (r"我(刚才|刚刚|之前|前面|上一句|上句话)说的是啥", 1.0),
            # 专门针对“上一句说了啥”等
            (r"上一句(我)?说了啥", 1.0),
            (r"上一句(我)?问了啥", 1.0),
            (r"上一句(我)?讲了啥", 1.0),
            (r"上句话(我)?说了啥", 1.0),
            (r"前一句(我)?说了啥", 1.0),
            # 其他明确表达
            (r"我上一句讲了什么", 1.0),
            (r"我前面那句(话)?说了什么", 1.0),
            (r"我前面那个问题是什么", 1.0),
            (r"我刚才提的问题是什么", 1.0),
            (r"我刚才讲的什么", 1.0),
            (r"我刚才提到的(内容|事情)是什么", 1.0),
            (r"我刚刚才说的(那句话|那个问题)是什么", 1.0),
            (r"我刚才是不是问了什么", 1.0),
            (r"我刚才是不是说了什么", 1.0),
            (r"我刚才说过的(话|内容)是什么", 1.0),
            (r"我(刚才|刚刚)说的什么来着", 0.9),  # 口语化，略低但足够

            # ===== 补充“是啥”的强模式（0.8-1.0分） =====
            (r"我前面说的(那个|那些)是啥", 0.9),
            (r"我上一句(问|说)的(那个|那些)是啥", 0.9),
            (r"我刚才(问|说)的(那个|那些)是啥", 0.9),
            (r"我刚刚(问|说)的那个(问题|话)是啥", 0.9),
            (r"我之前的(问题|话)是啥", 0.9),
            (r"我前面说的(内容|话|问题)是啥", 0.9),
            (r"我前面那个问题是啥", 0.9),
            (r"我刚才的问题是啥", 0.9),
            (r"我刚刚的问题是啥", 0.9),

            # ===== 请求回忆/重复（1.0分） =====
            (r"你(能)?(帮我)?(回忆|回想)一下我刚才(问了|说了)什么", 1.0),
            (r"你(能)?(帮我)?(回忆|回想)一下我刚才(问了|说了)啥", 1.0),
            (r"你记得我(刚才|刚刚|之前)问了什么吗", 1.0),
            (r"你记得我(刚才|刚刚|之前)说了什么吗", 1.0),
            (r"你记得我(刚才|刚刚|之前)问了啥吗", 1.0),
            (r"你记得我(刚才|刚刚|之前)说了啥吗", 1.0),
            (r"你(还)记得我上一句(问|说)的(什么|啥)吗", 1.0),
            (r"帮我(回忆|回想)一下我(刚才|刚刚|之前)的(问题|话)", 1.0),
            (r"能不能告诉我我刚才问了什么", 1.0),
            (r"你能重复一下我刚才的问题吗", 1.0),
            (r"可以再说一遍我刚才说了什么吗", 1.0),
            (r"你把我刚才说的重复一遍", 1.0),
            (r"请复述一下我刚才的(问题|话)", 1.0),

            # ===== 隐含询问前文（0.9分） =====
            (r"我说到哪了", 0.9),
            (r"我们刚才说到哪里了", 0.9),
            (r"刚才那个问题是什么", 0.9),
            (r"刚才那个话题是什么", 0.9),
            (r"刚刚讨论到什么了", 0.9),
            (r"我们刚刚聊到哪里了", 0.9),
            (r"之前说到哪了", 0.9),
            (r"前面提到什么了", 0.9),

            # ===== 中强模式（0.7-0.8分） =====
            # 包含“我”+时间词+关键词，但句式稍弱
            (r"我(刚才|刚刚|之前|前面)问过什么", 0.8),
            (r"我(刚才|刚刚|之前|前面)说过什么", 0.8),
            (r"我(刚才|刚刚|之前)的问题(是啥|是什么)", 0.8),
            (r"我(刚才|刚刚|之前)说的(内容|话|问题)是什么", 0.8),
            (r"我前面说的(那个|那些)是什么", 0.8),
            (r"我上一句(问|说)的(那个|那些)是什么", 0.8),
            (r"我刚才(问|说)的(那个|那些)是什么", 0.8),
            (r"我刚刚(问|说)的那个(问题|话)是什么", 0.8),
            (r"我之前的(问题|话)你还记得吗", 0.8),
            (r"我刚才说的你还记得吗", 0.8),
            (r"我之前问的你还有印象吗", 0.8),

            # 无“我”但有明确时间词+疑问词
            (r"(刚才|刚刚|之前)我说了什么", 0.7),
            (r"(刚才|刚刚|之前)我问了什么", 0.7),
            (r"(刚才|刚刚|之前)我的问题是什么", 0.7),
            (r"(刚才|刚刚|之前)我提的问题是什么", 0.7),
            (r"上一句(我)?说了什么", 0.7),
            (r"上一句(我)?问了什么", 0.7),
            (r"上句话(我)?说了什么", 0.7),
            (r"前一句(我)?说了什么", 0.7),
            (r"前面那句(话)是什么", 0.7),
            (r"刚刚那个问题是什么", 0.7),
            (r"刚才那个(问题|话)是什么", 0.7),

            # 提及“上下文”
            (r"我(刚才|刚刚|之前)的上下文是什么", 0.7),
            (r"上下文(是|指)什么", 0.7),
            (r"能告诉我上下文吗", 0.7),
            (r"我要看之前的上下文", 0.7),

            # ===== 弱模式（0.2-0.4分，用于组合） =====
            (r"我刚才", 0.3),
            (r"我刚刚", 0.3),
            (r"我之前", 0.3),
            (r"我前面", 0.3),
            (r"上一句", 0.3),
            (r"上句话", 0.3),
            (r"前一句", 0.3),
            (r"前面说的", 0.3),
            (r"问了什么", 0.4),
            (r"说了什么", 0.4),
            (r"问了啥", 0.4),
            (r"说了啥", 0.4),
            (r"问题是什么", 0.4),
            (r"什么来着", 0.4),
            (r"是什么", 0.2),
            (r"还记得吗", 0.3),
            (r"有印象吗", 0.3),
            (r"重复一遍", 0.4),
            (r"再说一遍", 0.4),
            (r"复述一下", 0.4),
            (r"上下文", 0.4),
            (r"前面的内容", 0.4),
            (r"之前的话题", 0.4),
            (r"刚才的话题", 0.4),
        ]

    def is_query(self, text):
        # 1. 检查负面规则
        for pattern in self.negative_rules:
            if re.search(pattern, text, re.IGNORECASE):
                return False

        # 2. 正面规则计分
        total_score = 0.0
        for pattern, score in self.positive_rules:
            if re.search(pattern, text, re.IGNORECASE):
                total_score += score
                if total_score >= self.threshold:
                    return True
        return total_score >= self.threshold

